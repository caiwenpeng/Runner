public class RunMouthData {


    private int id ;
    private String date;
    private float alltime;
    private float allcalorie;
    private float allvector;
    private float alldistance;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public float getAlltime() {
        return alltime;
    }

    public void setAlltime(float alltime) {
        this.alltime = alltime;
    }

    public float getAllcalorie() {
        return allcalorie;
    }

    public void setAllcalorie(float allcalorie) {
        this.allcalorie = allcalorie;
    }

    public float getAllvector() {
        return allvector;
    }

    public void setAllvector(float allvector) {
        this.allvector = allvector;
    }

    public float getAlldistance() {
        return alldistance;
    }

    public void setAlldistance(float alldistance) {
        this.alldistance = alldistance;
    }


}
